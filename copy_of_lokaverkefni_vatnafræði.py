import numpy as np
import matplotlib.pyplot as plt

def baseflow_filter(Q, alpha=0.925, passes=3):
    """Ladson et al. (2013) útfærsla á Lyne & Hollick síu."""
    q_f = np.zeros(len(Q))
    q_b = np.copy(Q)

    for _ in range(passes):
        for i in range(1, len(Q)):
            # Lyne & Hollick jafna
            q_f[i] = alpha * q_f[i-1] + (1 + alpha)/2 * (Q[i] - Q[i-1])
            q_f[i] = max(0, q_f[i]) # Quickflow má ekki vera neikvætt

        q_b = Q - q_f
        # Passa að grunnrennsli fari ekki yfir heildarrennsli
        q_b = np.where(q_b < 0, 0, q_b)
        q_b = np.where(q_b > Q, Q, q_b)
        Q = q_b # Fyrir næsta "pass"

    return q_b

