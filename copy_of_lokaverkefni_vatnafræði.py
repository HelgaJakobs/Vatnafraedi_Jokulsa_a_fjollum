rain_release_start = pd.to_datetime("2004-08-08")
rain_release_end = pd.to_datetime("2004-08-21")

heat_release_start = pd.to_datetime("2004-08-20")
heat_release_end = pd.to_datetime("2004-08-21")

# Reikna daga
time_to_peak_days = (q_peak - q_start).days
recession_days = (q_recession_end - q_peak).days
excess_rain_days = (rain_release_end - rain_release_start).days
excess_heat_days = (heat_release_end - heat_release_start).days

# Mynd
fig, ax1 = plt.subplots(figsize=(10,5))

# Rennsli
ax1.plot(event_Q["date"], event_Q["flow"], color="navy", linewidth=2)
ax1.set_ylabel("Rennsli (m³/s)", color="navy")
ax1.tick_params(axis='y', labelcolor="navy")
ax1.set_xlabel("Dagsetning")

# Hægri ás fyrir hitastig og úrkomu
ax2 = ax1.twinx()

# Hitastig
ax2.plot(event_T["date"], event_T["hitastig"], color="red", linewidth=2)

# Úrkoma
ax2.bar(event_P["date"], event_P["úrkoma"], width=0.8, alpha=0.35, color="blue")

ax2.set_ylabel("Hitastig (°C) og Úrkoma (mm)")

# Lóðréttar línur
ax1.axvline(q_start, color='black', linestyle='--', linewidth=1.2, alpha=0.8)
ax1.axvline(q_peak, color='black', linestyle='--', linewidth=1.8)
ax1.axvline(q_recession_end, color='black', linestyle='--', linewidth=1.2, alpha=0.8)

# Texti fyrir Qmax
ax1.text(q_peak + pd.Timedelta(days=0.2), ax1.get_ylim()[1]*0.97, "Qmax", fontsize=12, color="black", va="top")

# Hæðir fyrir örvar
y1 = event_Q["flow"].min() + 150
y2 = event_Q["flow"].min() + 90
y3 = event_Q["flow"].min() + 20
y4 = event_Q["flow"].min() + 90

# Time-to-peak
ax1.annotate( "", xy=(q_peak, y4), xytext=(q_start, y4), arrowprops=dict(arrowstyle="<->", color="darkgoldenrod", lw=2))
ax1.text(q_start + (q_peak - q_start)/2, y4 + 10, f"Time-to-peak\n({time_to_peak_days} dagar)", color="darkgoldenrod", ha="center", va="bottom", fontsize=12)

# Excess rain release time
ax1.annotate( "", xy=(rain_release_end, y3), xytext=(rain_release_start, y3), arrowprops=dict(arrowstyle="<->", color="forestgreen", lw=2))
ax1.text( rain_release_start + (rain_release_end - rain_release_start)/2, y3 + 10, f"Excess rain release time\n({excess_rain_days} dagar)", color="forestgreen", ha="center", va="bottom", fontsize=11)

# Excess heat release time
ax1.annotate( "", xy=(heat_release_end, y2), xytext=(heat_release_start, y2), arrowprops=dict(arrowstyle="<->", color="olivedrab", lw=2))
ax1.text( heat_release_start + (heat_release_end - heat_release_start)/2, y2 + 10, f"Excess heat release time\n({excess_heat_days} dagur)", color="olivedrab", ha="center", va="bottom", fontsize=11)

# Recession time
ax1.annotate( "", xy=(q_recession_end, y1), xytext=(q_peak, y1), arrowprops=dict(arrowstyle="<->", color="purple", lw=2))
ax1.text( q_peak + (q_recession_end - q_peak)/2, y1 + 10, f"Recession time\n({recession_days} dagar)", color="purple", ha="center", va="bottom", fontsize=12)

# Legend
legend_elements = [ Line2D([0], [0], color='navy', lw=2, label='Rennsli (Q)'), Line2D([0], [0], color='red', lw=2, label='Hitastig (T)'), Patch(facecolor='blue', edgecolor='blue', alpha=0.35, label='Úrkoma (P)')]
ax1.legend(handles=legend_elements, loc="upper left", frameon=True)

plt.title("Rennslisatburður 15. ágúst 2004")
fig.autofmt_xdate()
plt.tight_layout()
plt.show()
